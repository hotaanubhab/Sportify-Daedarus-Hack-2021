
let map,service, infoWindow,marker;


function initMap() {
    const latinput = document.getElementById("latitude");
    const longinput = document.getElementById("longitude");
    
  var myLatlng = { lat: 20.5937, lng:  78.9629 };
  var map = new google.maps.Map(document.getElementById("map"), {
    zoom: 4,
    center: myLatlng,
    // map-control:false
  });
  x=navigator.geolocation;
  x.getCurrentPosition(success,failure);
  function failure(){
    
  }
  function success(position) {

    myLatlng={ lat: position.coords.latitude, lng: position.coords.longitude };
    map.setOptions({
      center: myLatlng,
      zoom: 16
    });
  }

    google.maps.event.addListener(map, 'click', function(event) {
        placeMarker(event.latLng);
    });
    
    function placeMarker(location) {
        if(marker) {
            marker.setPosition(location);
        }
        else{
        marker = new google.maps.Marker({
            position: location,
            clickable: true,
            draggable: true, 
            map: map,
        });
        }
    }

//autocomplete places
var input = document.getElementById('searchInput');
    // map.controls[google.maps.ControlPosition.TOP_LEFT].push(input);
 

    var autocomplete = new google.maps.places.Autocomplete(input);
    autocomplete.bindTo('bounds', map);

    var infowindow = new google.maps.InfoWindow();
    if(marker)
    {
        marker.setMap(map);
        marker.setAnchorPoint(new google.maps.Point(0, -29 ));
    }
    else
    {
        marker = new google.maps.Marker({
        map: map,
        clickable: true,
        draggable: true, 
        anchorPoint: new google.maps.Point(0, -29 ),
        icon: {
            url:'https://maps.gstatic.com/mapfiles/place_api/icons/v1/png_71/geocode-71.png',
            size: new google.maps.Size(71, 71),
            origin: new google.maps.Point(0, 0),
            anchor: new google.maps.Point(17, 34),
            scaledSize: new google.maps.Size(35, 35)
        }  
       });
    }

    marker.addListener('position_changed',function(){
        latinput.value=marker.getPosition().lat();
        longinput.value=marker.getPosition().lng();
    })

    autocomplete.addListener('place_changed', function() {
        infowindow.close();
        marker.setVisible(true);
        var place = autocomplete.getPlace();
        if (!place.geometry) {
            window.alert("Autocomplete's returned place contains no geometry");
            return;
        }
  
        // If the place has a geometry, then present it on a map.
        if (place.geometry.viewport) {
            map.fitBounds(place.geometry.viewport);
        } else {
            map.setCenter(place.geometry.location);
            map.setZoom(17);
        }
        marker.setIcon(({
            url: place.icon,
            size: new google.maps.Size(71, 71),
            origin: new google.maps.Point(0, 0),
            anchor: new google.maps.Point(17, 34),
            scaledSize: new google.maps.Size(35, 35)
        }));
        marker.setPosition(place.geometry.location);
        marker.setVisible(true);
    
        var address = '';
        if (place.address_components) {
            address = [
              (place.address_components[0] && place.address_components[0].short_name || ''),
              (place.address_components[1] && place.address_components[1].short_name || ''),
              (place.address_components[2] && place.address_components[2].short_name || '')
            ].join(' ');
        }
    
        
      
    });
  
  // Add a style-selector control to the map.
  const styleControl = document.getElementById("style-selector-control");
  map.controls[google.maps.ControlPosition.TOP_LEFT].push(styleControl);
  // Set the map's style to the initial value of the selector.
  const styleSelector = document.getElementById("style-selector");
  map.setOptions({ styles: styles[styleSelector.value] });
  // Apply new JSON when the user selects a different style.
  styleSelector.addEventListener("change", () => {
    map.setOptions({ styles: styles[styleSelector.value] });
  });


 //drawing functions

  const drawingManager = new google.maps.drawing.DrawingManager({
    drawingMode: null,
    drawingControl: false,
    drawingControlOptions: {
      position: google.maps.ControlPosition.TOP_CENTER,
      drawingModes: [
        google.maps.drawing.OverlayType.MARKER,
        google.maps.drawing.OverlayType.CIRCLE,
        google.maps.drawing.OverlayType.POLYGON,
        google.maps.drawing.OverlayType.POLYLINE,
        google.maps.drawing.OverlayType.RECTANGLE,
      ],
    },
    markerOptions: {
      icon: "https://developers.google.com/maps/documentation/javascript/examples/full/images/beachflag.png",
    },
    circleOptions: {
      fillColor: "#ffff00",
      fillOpacity: 1,
      strokeWeight: 5,
      clickable: false,
      editable: true,
      zIndex: 1,
    },
  });
  drawingManager.setMap(map);

}


